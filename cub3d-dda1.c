#include "cub3d.h"

int	cub_dda_ns(t_ray *ray)
{
	if (ray->dir[1] < 0.0f)
		return (1);
	return (3);
}

int	cub_dda_we(t_ray *ray)
{
	if (ray->dir[0] < 0.0f)
		return (0);
	return (2);
}
